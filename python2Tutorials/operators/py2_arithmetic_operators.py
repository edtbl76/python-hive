from __future__ import print_function


def main():
    """ Operators """

    # Addition
    print("10 + 10 = {}".format(10 + 10))

    # Subtraction
    print("10 - 10 = {}".format(10 - 10))

    # Multiplication
    print("10 * 10 = {}".format(10 * 10))

    # Division
    print("10 / 3  = {}".format(10 / 3))

    # Modulus
    print("10 % 3  = {}".format(10 % 3))

    # Floor/Truncation Division
    print("10 // 3 = {}".format(10 // 3))

    # Positive Unary
    print("+10     = {}".format(+10))

    # Negative Unary
    print("-10     = {}".format(-10))

    # Exponentiation
    print("2^8     = 2 ** 8 = {}".format(2 ** 8))



if __name__ == '__main__':
    main()
